package model;

/**
 *
 * @author Admin
 */
public class Signup {

    String role;
    String user;
    String pass;
    String repass;
    String email;
    String fullname;
    String phone;
    String birth;
    String address;
    String gender;

    public Signup() {
    }

    public Signup(String role, String user, String pass, String repass, String email, String fullname, String phone, String birth, String address, String gender) {
        this.role = role;
        this.user = user;
        this.pass = pass;
        this.repass = repass;
        this.email = email;
        this.fullname = fullname;
        this.phone = phone;
        this.birth = birth;
        this.address = address;
        this.gender = gender;
    }

    public String getRole() {
        return role;
    }

    public void setRole(String role) {
        this.role = role;
    }

    public String getUser() {
        return user;
    }

    public void setUser(String user) {
        this.user = user;
    }

    public String getPass() {
        return pass;
    }

    public void setPass(String pass) {
        this.pass = pass;
    }

    public String getRepass() {
        return repass;
    }

    public void setRepass(String repass) {
        this.repass = repass;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getFullname() {
        return fullname;
    }

    public void setFullname(String fullname) {
        this.fullname = fullname;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getBirth() {
        return birth;
    }

    public void setBirth(String birth) {
        this.birth = birth;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getGender() {
        return gender;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }
    
    
}
